/* ========================== CRC-8 计算 ========================== */

uint8_t Proto_CalcCRC8(const uint8_t *data, uint8_t len)
{
    uint8_t crc = 0x00;
    while (len--)
        crc = crc8_table[crc ^ *data++];
    return crc;
}

/* ========================== 帧构建 ========================== */

void Proto_BuildAlertFrame(Proto_AlertFrame_t *frame, const AlertEvent_t *event)
{
    if (!frame || !event) return;

    Proto_AlertFrameRaw_t raw;
    memset(&raw, 0, sizeof(raw));

    /* 同步字 */
    raw.sync_h = PROTO_SYNC_WORD_H;
    raw.sync_l = PROTO_SYNC_WORD_L;

    /* 设备 ID (大端) */
    raw.device_id = ((PROTO_DEVICE_ID & 0xFF) << 8) | ((PROTO_DEVICE_ID >> 8) & 0xFF);

    /* 报警类型 & 级别 */
    raw.alert_type  = (uint8_t)event->type;
    raw.alert_level = (uint8_t)event->level;

    /* 报警值 (大端) */
    raw.value_h = (uint8_t)((event->value >> 8) & 0xFF);
    raw.value_l = (uint8_t)(event->value & 0xFF);

    /* 时间戳 (大端) */
    raw.timestamp = ((event->timestamp_ms & 0xFF) << 24)
                  | ((event->timestamp_ms & 0xFF00) << 8)
                  | ((event->timestamp_ms & 0xFF0000) >> 8)
                  | ((event->timestamp_ms >> 24) & 0xFF);

    /* 消息 (最多 8 字符) */
    strncpy((char *)raw.message, event->message, 8);

    /* CRC-8 (覆盖除 CRC 自身外的所有字段) */
    raw.crc8 = Proto_CalcCRC8((uint8_t *)&raw, sizeof(raw) - 1);

    /* 打包到输出帧 */
    memcpy(frame->raw_data, &raw, sizeof(raw));
    frame->length = sizeof(raw);
}

/* ========================== 帧解析 (接收端使用) ========================== */

uint8_t Proto_ParseAlertFrame(const uint8_t *data, uint8_t len, AlertEvent_t *event)
{
    if (!data || !event || len < sizeof(Proto_AlertFrameRaw_t))
        return 0;

    Proto_AlertFrameRaw_t *raw = (Proto_AlertFrameRaw_t *)data;

    /* 同步字校验 */
    if (raw->sync_h != PROTO_SYNC_WORD_H || raw->sync_l != PROTO_SYNC_WORD_L)
        return 0;

    /* CRC 校验 */
    uint8_t calc_crc = Proto_CalcCRC8(data, sizeof(Proto_AlertFrameRaw_t) - 1);
    if (calc_crc != raw->crc8)
        return 0;

    /* 解析到事件结构 */
    event->type  = (AlertType_t)raw->alert_type;
    event->level = (AlertLevel_t)raw->alert_level;
    event->value = ((uint16_t)raw->value_h << 8) | raw->value_l;

    /* 时间戳 (大端→小端) */
    event->timestamp_ms = ((uint32_t)raw->timestamp << 24)
                        | ((uint32_t)raw->timestamp << 8  & 0x00FF0000)
                        | ((uint32_t)raw->timestamp >> 8  & 0x0000FF00)
                        | ((uint32_t)raw->timestamp >> 24 & 0x000000FF);

    memcpy(event->message, raw->message, 8);
    event->message[8] = '\0';

    return 1;
}
